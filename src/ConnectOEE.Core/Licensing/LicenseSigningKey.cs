namespace ConnectOEE.Core.Licensing;

internal static class LicenseSigningKey
{
    private static readonly byte[] OeeEncoded =
    [
        0xDD, 0x6A, 0x68, 0x14, 0x53, 0xB2, 0x26, 0xE3, 0x5E, 0x48, 0x2C, 0x46, 0x77, 0xAC, 0x7D, 0x5B,
        0x3E, 0xF0, 0x90, 0x8F, 0x64, 0x09, 0xBB, 0x5E, 0xFD, 0xC6, 0x07, 0x2B, 0x5F, 0x32, 0x02, 0x05
    ];

    internal static byte[] GetHmacKeyBytes()
    {
        var key = new byte[OeeEncoded.Length];
        for (var i = 0; i < OeeEncoded.Length; i++)
            key[i] = (byte)(OeeEncoded[i] ^ 0xA5);
        return key;
    }
}
